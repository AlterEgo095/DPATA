// ============================================================================
// PASSWORD HASHING (BCRYPT - Production Ready)
// ============================================================================

/**
 * Hash a password using bcrypt (industry standard)
 * - Uses salt automatically generated by bcrypt
 * - Cost factor of 12 (2^12 = 4096 rounds)
 * - Resistant to rainbow tables and brute force
 */
export async function hashPassword(password: string): Promise<string> {
  return bcrypt.hash(password, BCRYPT_ROUNDS);
}

/**
 * Verify a password against a bcrypt hash
 * - Timing-safe comparison (prevents timing attacks)
 * - Works with both bcrypt hashes and legacy SHA256 (migration support)
 */
export async function verifyPassword(password: string, hash: string): Promise<boolean> {
  // Check if it's a legacy SHA64 hash (64 hex chars) or bcrypt hash ($2a$/$2b$ prefix)
  if (hash.startsWith('$2a$') || hash.startsWith('$2b$') || hash.startsWith('$2y$')) {
    // Modern bcrypt hash
    return bcrypt.compare(password, hash);
  } else if (hash.length === 64 && /^[a-f0-9]{64}$/i.test(hash)) {
    // Legacy SHA256 hash - verify and recommend migration
    const legacyHash = crypto.createHash('sha256').update(password + (process.env.PASSWORD_SALT || 'dpata-salt-2024')).digest('hex');
    return legacyHash === hash;
  }
  // Fallback for plain text passwords (should not happen in production)
  return password === hash;
}

/**
 * Migrate a legacy password hash to bcrypt
 * Call this after successful login with legacy hash
 */
export async function migratePasswordHash(password: string): Promise<string> {
  return hashPassword(password);
}

// ============================================================================
// INPUT SANITIZATION (XSS Prevention)
// ============================================================================

/**
 * Sanitize string input to prevent XSS attacks
 * Escapes HTML special characters
 */
export function sanitizeInput(str: string): string {
  if (typeof str !== 'string') return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#x27;')
    .replace(/\//g, '&#x2F;')
    .trim();
}

/**
 * Sanitize object recursively - all string values are escaped
 */
export function sanitizeObject(obj: Record<string, any>): Record<string, any> {
  const sanitized: Record<string, any> = {};
  for (const [key, value] of Object.entries(obj)) {
    if (typeof value === 'string') {
      sanitized[key] = sanitizeInput(value);
    } else if (Array.isArray(value)) {
      sanitized[key] = value.map(item => 
        typeof item === 'string' ? sanitizeInput(item) : 
        typeof item === 'object' && item !== null ? sanitizeObject(item) : item
      );
    } else if (typeof value === 'object' && value !== null) {
      sanitized[key] = sanitizeObject(value);
    } else {
      sanitized[key] = value;
    }
  }
  return sanitized;
}

