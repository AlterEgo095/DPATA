/**
 * Validate email format
 */
export function isValidEmail(email: string): boolean {
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  return emailRegex.test(email);
}

/**
 * Validate password strength
 * Returns object with isValid and reasons for invalid passwords
 */
export function validatePasswordStrength(password: string): {
  isValid: boolean;
  strength: 'weak' | 'medium' | 'strong';
  errors: string[];
} {
  const errors: string[] = [];
  
  if (password.length < 8) errors.push('Minimum 8 caractères');
  if (password.length > 128) errors.push('Maximum 128 caractères');
  if (!/[A-Z]/.test(password)) errors.push('Une majuscule requise');
  if (!/[a-z]/.test(password)) errors.push('Une minuscule requise');
  if (!/[0-9]/.test(password)) errors.push('Un chiffre requis');
  
  let strength: 'weak' | 'medium' | 'strong' = 'weak';
  if (password.length >= 12 && /[A-Z]/.test(password) && /[a-z]/.test(password) && /[0-9]/.test(password) && /[^A-Za-z0-9]/.test(password)) {
    strength = 'strong';
  } else if (password.length >= 8 && ((/[A-Z]/.test(password) && /[a-z]/.test(password)) || /[0-9]/.test(password))) {
    strength = 'medium';
  }
  
  return {
    isValid: errors.length === 0,
    strength,
    errors,
  };
}

// ============================================================================
// ERROR SANITIZATION (Prevent information leakage)
// ============================================================================

/**
 * Sanitize error message for production - prevent leaking internal details
 */
export function sanitizeError(error: unknown): { error: string; code?: string } {
  if (error instanceof Error) {
    // In production, don't expose internal error details
    if (process.env.NODE_ENV === 'production') {
      console.error('[ERROR]', error.message); // Log internally
      return { error: 'Une erreur est survenue', code: 'INTERNAL_ERROR' };
    }
    // In development, provide more details
    return { error: error.message, code: error.name };
  }
  return { error: 'Une erreur inconnue est survenue' };
}
